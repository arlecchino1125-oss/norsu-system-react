-- Reused by every student-facing policy below: attended the event and timed out.
CREATE OR REPLACE FUNCTION public.student_may_evaluate_form(p_form_id bigint) RETURNS boolean
    LANGUAGE sql
    STABLE
    SECURITY DEFINER
    SET search_path = 'public'
    AS $$
    SELECT EXISTS (
        SELECT 1
        FROM public.event_evaluation_forms f
        JOIN public.event_attendance a ON a.event_id = f.event_id
        WHERE f.id = p_form_id
          AND f.event_id IS NOT NULL
          AND f.is_active
          AND a.student_id = public.current_student_id()
          AND a.time_out IS NOT NULL
    );
$$;

REVOKE EXECUTE ON FUNCTION public.student_may_evaluate_form(bigint) FROM anon;

-- Forms: staff manage everything; a student sees only the evaluation for an
-- event they attended. Templates (event_id IS NULL) are structurally invisible
-- to students -- there is no policy that can return one.
CREATE POLICY "event_evaluation_forms_staff_all"
    ON public.event_evaluation_forms
    FOR ALL
    TO authenticated
    USING (public.is_admin() OR public.current_staff_role() = 'Care Staff'::text)
    WITH CHECK (public.is_admin() OR public.current_staff_role() = 'Care Staff'::text);

CREATE POLICY "event_evaluation_forms_student_read_attended"
    ON public.event_evaluation_forms
    FOR SELECT
    TO authenticated
    USING (public.student_may_evaluate_form(id));

CREATE POLICY "event_evaluation_questions_staff_all"
    ON public.event_evaluation_questions
    FOR ALL
    TO authenticated
    USING (public.is_admin() OR public.current_staff_role() = 'Care Staff'::text)
    WITH CHECK (public.is_admin() OR public.current_staff_role() = 'Care Staff'::text);

CREATE POLICY "event_evaluation_questions_student_read"
    ON public.event_evaluation_questions
    FOR SELECT
    TO authenticated
    USING (public.student_may_evaluate_form(form_id));

CREATE POLICY "event_evaluation_responses_staff_all"
    ON public.event_evaluation_responses
    FOR ALL
    TO authenticated
    USING (public.is_admin() OR public.current_staff_role() = 'Care Staff'::text)
    WITH CHECK (public.is_admin() OR public.current_staff_role() = 'Care Staff'::text);

CREATE POLICY "event_evaluation_responses_student_insert_own"
    ON public.event_evaluation_responses
    FOR INSERT
    TO authenticated
    WITH CHECK (student_id = public.current_student_id() AND public.student_may_evaluate_form(form_id));

CREATE POLICY "event_evaluation_responses_student_read_own"
    ON public.event_evaluation_responses
    FOR SELECT
    TO authenticated
    USING (student_id = public.current_student_id());

CREATE POLICY "event_evaluation_answers_staff_all"
    ON public.event_evaluation_answers
    FOR ALL
    TO authenticated
    USING (public.is_admin() OR public.current_staff_role() = 'Care Staff'::text)
    WITH CHECK (public.is_admin() OR public.current_staff_role() = 'Care Staff'::text);

CREATE POLICY "event_evaluation_answers_student_insert_own"
    ON public.event_evaluation_answers
    FOR INSERT
    TO authenticated
    WITH CHECK (EXISTS (
        SELECT 1 FROM public.event_evaluation_responses r
        WHERE r.id = event_evaluation_answers.response_id
          AND r.student_id = public.current_student_id()
    ));

CREATE POLICY "event_evaluation_answers_student_read_own"
    ON public.event_evaluation_answers
    FOR SELECT
    TO authenticated
    USING (EXISTS (
        SELECT 1 FROM public.event_evaluation_responses r
        WHERE r.id = event_evaluation_answers.response_id
          AND r.student_id = public.current_student_id()
    ));

GRANT SELECT, INSERT, UPDATE, DELETE ON public.event_evaluation_forms TO authenticated;
GRANT SELECT, INSERT, UPDATE, DELETE ON public.event_evaluation_questions TO authenticated;
GRANT SELECT, INSERT, UPDATE, DELETE ON public.event_evaluation_responses TO authenticated;
GRANT SELECT, INSERT, UPDATE, DELETE ON public.event_evaluation_answers TO authenticated;
GRANT ALL ON public.event_evaluation_forms TO service_role;
GRANT ALL ON public.event_evaluation_questions TO service_role;
GRANT ALL ON public.event_evaluation_responses TO service_role;
GRANT ALL ON public.event_evaluation_answers TO service_role;

