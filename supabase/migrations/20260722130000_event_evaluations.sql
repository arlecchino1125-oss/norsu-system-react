-- Staff-built evaluation forms attached to events.
--
-- Deliberately separate from needs_assessment_* rather than sharing it: that
-- table set has exactly one reader policy ("any authenticated user may read an
-- active form"), which is wrong here -- an event evaluation must only reach
-- students who actually attended. Fresh tables get a policy written for the job.
--
-- Coexists with event_feedback (the fixed 7-question rating); it does not
-- replace it.

-- A form with event_id IS NULL is a reusable template; with an event_id it is
-- that event's live evaluation. UNIQUE(event_id) gives "one evaluation per
-- event" and unlimited templates in one index, because Postgres treats NULLs as
-- distinct. Attaching a template COPIES its rows, so editing a template later
-- can never change the meaning of answers already recorded.
CREATE TABLE public.event_evaluation_forms (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_id bigint REFERENCES public.events(id) ON DELETE CASCADE,
    title text NOT NULL,
    description text,
    is_active boolean NOT NULL DEFAULT true,
    created_at timestamp with time zone NOT NULL DEFAULT timezone('utc'::text, now()),
    CONSTRAINT event_evaluation_forms_title_len CHECK (char_length(title) BETWEEN 1 AND 200),
    CONSTRAINT event_evaluation_forms_description_len CHECK (description IS NULL OR char_length(description) <= 1000)
);

CREATE UNIQUE INDEX event_evaluation_forms_one_per_event
    ON public.event_evaluation_forms (event_id)
    WHERE event_id IS NOT NULL;

CREATE TABLE public.event_evaluation_questions (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    form_id bigint NOT NULL REFERENCES public.event_evaluation_forms(id) ON DELETE CASCADE,
    order_index integer NOT NULL DEFAULT 0,
    question_text text NOT NULL,
    question_type text NOT NULL,
    scale_min integer,
    scale_max integer,
    scale_min_label text,
    scale_max_label text,
    choices text[] NOT NULL DEFAULT '{}'::text[],
    is_required boolean NOT NULL DEFAULT true,
    CONSTRAINT event_evaluation_questions_text_len CHECK (char_length(question_text) BETWEEN 1 AND 500),
    CONSTRAINT event_evaluation_questions_type_chk CHECK (question_type = ANY (ARRAY['scale'::text, 'text'::text, 'choice'::text])),
    -- Each type carries its own required settings. Enforced here so a
    -- half-built question cannot reach a student's screen and render blank.
    CONSTRAINT event_evaluation_questions_shape_chk CHECK (
        CASE question_type
            WHEN 'choice' THEN coalesce(array_length(choices, 1), 0) >= 2
            WHEN 'scale' THEN scale_min IS NOT NULL AND scale_max IS NOT NULL AND scale_max > scale_min
            ELSE true
        END
    )
);

CREATE INDEX event_evaluation_questions_form_id_idx
    ON public.event_evaluation_questions (form_id, order_index);

-- Identity columns are a snapshot, not a join: a student's course and year
-- change between school years, and an evaluation must keep reading the way it
-- read on the day it was submitted. Stamped by trigger, never by the client.
CREATE TABLE public.event_evaluation_responses (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    form_id bigint NOT NULL REFERENCES public.event_evaluation_forms(id) ON DELETE CASCADE,
    student_id character varying NOT NULL REFERENCES public.students(student_id) ON UPDATE CASCADE,
    student_name text,
    department text,
    course text,
    year_level text,
    submitted_at timestamp with time zone NOT NULL DEFAULT timezone('utc'::text, now()),
    CONSTRAINT event_evaluation_responses_one_per_student UNIQUE (form_id, student_id)
);

CREATE INDEX event_evaluation_responses_form_id_idx
    ON public.event_evaluation_responses (form_id);

CREATE TABLE public.event_evaluation_answers (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    response_id bigint NOT NULL REFERENCES public.event_evaluation_responses(id) ON DELETE CASCADE,
    question_id bigint NOT NULL REFERENCES public.event_evaluation_questions(id) ON DELETE CASCADE,
    answer_value integer,
    answer_text text,
    CONSTRAINT event_evaluation_answers_one_per_question UNIQUE (response_id, question_id),
    CONSTRAINT event_evaluation_answers_text_len CHECK (answer_text IS NULL OR char_length(answer_text) <= 1500)
);

CREATE INDEX event_evaluation_answers_response_id_idx
    ON public.event_evaluation_answers (response_id);

-- The student controls the request body, not just the row, so RLS alone cannot
-- stop them posting someone else's name or a flattering department. Stamp all
-- identity fields server-side for students; staff corrections pass through.
-- Same pattern as stamp_peer_facilitator_attendance_times.
CREATE OR REPLACE FUNCTION public.stamp_event_evaluation_response_identity() RETURNS trigger
    LANGUAGE plpgsql
    SET search_path = 'public'
    AS $$
DECLARE
    v_student_id text := public.current_student_id();
BEGIN
    -- Non-students (Care Staff, Admin, service role) keep their supplied values.
    IF v_student_id IS NULL THEN
        RETURN NEW;
    END IF;

    NEW.student_id := v_student_id;
    NEW.submitted_at := now();

    SELECT trim(concat_ws(' ', s.first_name, s.last_name)), s.department, s.course, s.year_level
    INTO NEW.student_name, NEW.department, NEW.course, NEW.year_level
    FROM public.students s
    WHERE s.student_id = v_student_id;

    RETURN NEW;
END;
$$;

REVOKE EXECUTE ON FUNCTION public.stamp_event_evaluation_response_identity() FROM anon;

CREATE TRIGGER event_evaluation_responses_stamp_identity
    BEFORE INSERT ON public.event_evaluation_responses
    FOR EACH ROW
    EXECUTE FUNCTION public.stamp_event_evaluation_response_identity();

